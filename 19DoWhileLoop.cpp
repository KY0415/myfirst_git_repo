//  Do while loop syntex

/*

initialization;
do{
    statement;
    increment/decrement;
}while(condition);

*/

#include<iostream>
using namespace std;
int main()
{
    int i=1;
    do
    {
        cout<<"Allahu Akbar: "<<i<<endl;
        i++;
    }
    while(i<=9);

    return 0;
}

//  The "do while" loop will execute at least one time even if the condition is false....
