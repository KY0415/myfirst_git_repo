#include<iostream>
#include<string>
using namespace std;
int main()
{
    int a = 10;
    int b = 20;
    
    int c = a + b;  // for mathematical operation (addition)...     c will be 30 (an integer)...

    cout << c << endl;

    string x = "10";
    string y = "20";

    string z = x + y;   // here, it is concatenation...     z will be 1020 (an string)...

    cout << z << endl;

    

    return 0;
}